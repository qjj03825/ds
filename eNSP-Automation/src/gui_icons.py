def get_icon_image(icon_data):
    """从Base64数据创建Tkinter图像对象
    
    Args:
        icon_data: Base64编码的图像数据
        
    Returns:
        Tkinter PhotoImage对象
    """
    binary_data = base64.b64decode(icon_data)
    image = PhotoImage(data=binary_data)
    return image 